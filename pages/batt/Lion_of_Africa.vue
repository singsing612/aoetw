              </li>
              <li>
                營帳位置：遠征軍的山壁正上方1個→山壁右上方1個→往右直線走到底1個→貼著地圖邊邊往上走到底會碰到2個
              </li>
              <li>
                黃色和綠色兵力可用僧侶<nuxt-link to="/elements/Conversion"
                  >招降</nuxt-link
                >，尤其黃色很好招。初期遠征軍會有一名僧侶，可於攻打黃色營帳期間加減招降增添我方單位。
              </li>
              <li>
                遠征軍的聖物推車，可使用運輸傳送回島上，並放置城堡旁（功效不明）。
