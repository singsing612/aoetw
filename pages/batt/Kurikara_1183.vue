          <h3>歷史</h3>
          <div class="hrn1"></div>
          <ul>
            <li>
              這場戰役描寫的其實不只是俱利伽羅峠之戰一役而已，而是從那次戰役一直到攻陷京都。
            </li>
            <li>
              俱利伽羅峠指的是越<nuxt-link to="/civs/Chinese">中國</nuxt-link
              >與加賀國的邊境關口，是個險要的斷崖。
            </li>
            <li>源氏其實就是源賴朝，真正的源家掌權者。</li>
            <li>
              木曾義仲的本名是源義仲，是源賴朝的表親，因為在木曾谷長大而得此名。
            </li>
            <li>
              歷史上的北条時政是源賴朝的岳父，但其實跟木曾義仲不合，在真正的俱利伽羅峠之戰也不曾出手幫助過木曾義仲。後來甚至親手消滅了義仲的勢力。
            </li>
            <li>
              本戰役的最終大魔王平清盛，歷史上其實早在這場戰役發生的兩年前死於熱病。而歷史上真正領導這場戰役的平維盛則連露臉都沒有。
            </li>
            <li>
              歷史上真正守衛能登國（遊戲中的"燈塔"）的是平通盛與平忠度，而不是平清房。
            </li>
            <li>
              據說木曾義仲在此戰中曾使用中國田單的火牛戰術，後來事後認為是捏造。
            </li>
            <li>
              京都在古代日本被分類為畿內，其實在歷史上沒有這麼堅固，反而因為地處平原周圍環山而常常被攻陷。
            </li>
            <li>遊戲地圖就是整個日本中部，但是形狀跟比例似乎怪怪的。</li>
            <li>本次大魔王平清盛其實根本沒當過王，他至多只能算個國防部長。</li>
            <li>源賴朝本人沒有參與俱利伽羅峠之戰。</li>
            <li>小魔王之一的平知盛其實活到了源平合戰的最後一戰。</li>
            <li>
              歷史上的木曾義仲確實曾經進軍並掌管京都，但是卻因為剛愎自用與縱容軍隊暴行，最終被源家討伐。
            </li>
            <li>真正的平氏在1185年的壇之浦之戰滅亡。</li>
            <li>
              源家的勢力在1333年與鎌倉幕府一起進入了歷史，前北条家也滅亡（後北条就是戰國有名的北条早雲，世紀帝國II日本AI的常用名稱）。
            </li>
            <li>
              源平合戰最終導致日本真正進入數百年的幕府時代，也可以算是封建時代的開端。
            </li>
          </ul>
