                  >與<nuxt-link to="/building/Archery_Range">射箭場</nuxt-link
                  >(至少各5個)。如果玩家這時耗盡金礦，可以研發市集科技公會制度然後開始從市場銷售買賣資源。最終玩家會殺了他們的<nuxt-link
                    to="/units/hero"
                    >英雄</nuxt-link
                  >(橘色的奧斯曼首領土耳其帕莎)而奧斯曼人將被擊敗。這時不要破壞他們的市集，切換與奧斯曼帝國的外交為“中立”並開始貿易，把奧斯曼城池的城門都摧毀改成我方城門防禦(不要打太多奧斯曼城池城牆，可以協防)在他們的市集旁邊建造一座城堡以保護您的<nuxt-link
                    to="/units/Trade_Cart"
                    >貿易車隊</nuxt-link
                  >免遭襲擊。
                </li>
