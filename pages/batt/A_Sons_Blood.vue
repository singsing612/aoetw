                  最後是馬木留克人，生產<nuxt-link
                    to="/units/Heavy_Demolition_Ship"
                    >重型爆破船</nuxt-link
                  >從海上進攻他們的碼頭，以阻擋他們不斷生產的火戰船，此時火炮戰船可以繼續炮轟他們的箭塔與其他建築。一旦馬木留克失去戰鬥能力，他會莫名的大量生產許多運輸艦(如果他還有碼頭還未摧毀的話)。玩家取得海上控制權的同時，陸面部隊也可開始進軍會師掃蕩敵軍。持續用火炮戰船繼續攻擊射程內的敵軍建築。
                </li>
