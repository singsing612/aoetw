                  >軍隊向玩家襲來，此時需要一支強大的陸面作戰部隊;訓練強弩兵（用來壓制土耳其火槍兵），和火砲反制他們!此外，馬木留克將會支援奧斯曼帝國的軍隊，所以讓戟兵保護你的前線。
                </li>
                <li>
                  為了控制與奧斯曼陣地間的河流主權，玩家可以部屬卡拉維爾戰船由外駛近河流打擊奧斯曼的路面部隊，封鎖河流入口點，但還是要小心奧斯曼的火砲打擊我方戰艦。這時海面還是會有馬木留克的海軍在海灣上支援奧斯曼，而玩家的海軍可以在河岸近海灘上下航行，利用火炮戰船摧毀射程範圍範圍內的敵軍<nuxt-link
                    to="/building"
                    >建築物</nuxt-link
                  >，尤其是防禦塔類的建築和城堡。在土耳其人被削弱之後，才能和陸軍一起進來會師。此時可研發帝王時代<nuxt-link
                    to="/techs/untech"
                    >特殊科技</nuxt-link
