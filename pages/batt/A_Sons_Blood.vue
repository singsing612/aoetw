                  推進馬木留克人的陣地，讓陸面部隊與攻城武器協助控制海灣，較內陸的城堡還是需要攻城武器擊破，馬木留克會有奴隸兵與<nuxt-link
                    to="/units/Condottiero"
                    >義大利傭兵</nuxt-link
                  >組成的軍隊迎擊。切入到內陸將會看到城堡旁有馬木留克首領阿米爾侯賽因阿爾庫爾迪，玩家只需用<nuxt-link
