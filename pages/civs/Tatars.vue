        <h3>歷史</h3>
        <div class="hrn1"></div>
        <div class="n1">
          <p>
            十一世紀，韃靼人開始與蒙古人進行激烈的戰爭。然而，成吉思汗擊敗了韃靼人並建立了有史以來最大的帝國之一。成吉思汗去世後，韃靼人建立了許多蒙古帝國的繼承國，比如金帳汗國（1227-1502）。在拔都汗的統治下，韃靼人向西擴張並征服了庫曼人、伏爾加保加利亞、基輔羅斯和東歐。由於征服了許多突厥部落，在時間的推移下，韃靼開始突厥化。後來月即別治下的金帳汗國完成伊斯蘭化並遷都，使該城市成為該地區最大的城市之一。月即別去世後，金帳汗國開始衰落，1340年代的黑死病更破壞了韃靼人的經濟，從此淪為規模較小的汗國。14世紀末，隨著蒙古帝國的瓦解，韃靼軍閥帖木兒利用他的軍事天份接管了查加泰汗國，後來建立了帖木兒帝國（1370-1507）。隨後的幾十年中，他通過一系列血腥戰役征服了中亞大部分地區。然而，帖木兒也是一位積極的文化贊助人，他與整個帝國的技術工匠聚集在撒馬爾罕，促進文化和藝術互動。比如帖木兒掌握了以藍色和青綠色瓷磚的圓頂和幾何圖案為特色的塞爾柱建築風格，其中帖木兒的陵墓古爾埃米爾被認為是波斯-蒙古建築的頂峰。帖木兒去世後，帖木兒帝國開始衰落，韃靼人統治的時代也結束了。
          </p>
