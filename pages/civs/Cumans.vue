            Kotyan）試圖通過與羅斯結盟來抵抗，但在1223年於卡利奇克河遭受慘敗，許多庫曼人向各處流亡並被當地居民吸收，但更多人是被俘虜作為奴隸出售。埃及蘇丹沙伊爾阿尤比（Al-Sahil
            Ayyubi）購買了許多庫曼人和欽察人訓練為馬穆魯克。1250年，馬穆魯克利用自己的軍事力量奪取了埃及的政權，並建立了馬穆魯克蘇丹國。
